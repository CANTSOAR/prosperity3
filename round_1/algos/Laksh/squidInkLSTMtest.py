class Trader:
    def run(self, state: TradingState):
        # Print initial traderData and observations for debugging.
        print("traderData:", state.traderData)
        print("Observations:", str(state.observations))
        
        # Load persistent state.
        try:
            trader_state = jsonpickle.decode(state.traderData)
            if not isinstance(trader_state, dict):
                trader_state = {}
        except Exception:
            trader_state = {}
        
        # Initialize persistent data for SQUID_INK if not present.
        if "squid_ml" not in trader_state:
            trader_state["squid_ml"] = {"price_history": []}
        ml_state = trader_state["squid_ml"]
        
        result = {}  # Dictionary to hold orders.
        
        # Loop over products in order_depths.
        for product in state.order_depths:
            order_depth: OrderDepth = state.order_depths[product]
            
            # ---------------------------
            # Strategy for RAINFOREST_RESIN:
            # Use fixed acceptable price (10000).
            # ---------------------------
            if product == "RAINFOREST_RESIN":
                orders: List[Order] = []
                acceptable_price = 10000
                print("RAINFOREST_RESIN - Acceptable Price:", acceptable_price)
                print("RAINFOREST_RESIN - Buy Order Depth:", len(order_depth.buy_orders),
                      "Sell Order Depth:", len(order_depth.sell_orders))
                if len(order_depth.sell_orders) != 0:
                    best_ask, best_ask_amount = list(order_depth.sell_orders.items())[0]
                    if int(best_ask) < acceptable_price:
                        print("RAINFOREST_RESIN - BUY order: Buying", -best_ask_amount, "units at", best_ask)
                        orders.append(Order(product, best_ask, -best_ask_amount))
                if len(order_depth.buy_orders) != 0:
                    best_bid, best_bid_amount = list(order_depth.buy_orders.items())[0]
                    if int(best_bid) > acceptable_price:
                        print("RAINFOREST_RESIN - SELL order: Selling", best_bid_amount, "units at", best_bid)
                        orders.append(Order(product, best_bid, -best_bid_amount))
                result[product] = orders
            
            # ---------------------------
            # Strategy for SQUID_INK: LSTM-based prediction.
            # ---------------------------
            elif product == "SQUID_INK":
                orders: List[Order] = []
                # Determine current mid price from order book.
                if order_depth.buy_orders and order_depth.sell_orders:
                    best_bid = max(order_depth.buy_orders.keys())
                    best_ask = min(order_depth.sell_orders.keys())
                    current_mid = (best_bid + best_ask) / 2
                else:
                    current_mid = global_mean  # Fallback using global_mean.
                print("SQUID_INK - Current mid price:", current_mid)
                
                # Update rolling price history: store last 6 mid prices.
                ml_state["price_history"].append(current_mid)
                if len(ml_state["price_history"]) > 6:
                    ml_state["price_history"] = ml_state["price_history"][-6:]
                
                if len(ml_state["price_history"]) < 6:
                    print("SQUID_INK - Not enough history for LSTM prediction. Skipping trade.")
                else:
                    # Form input vector from last 6 mid prices.
                    x_seq = np.array(ml_state["price_history"], dtype=np.float32)  # shape (6,)
                    # Normalize using global normalization parameters.
                    x_norm = (x_seq - global_mean) / global_std
                    # Run LSTM forward pass.
                    y_norm_pred = lstm_forward(x_norm)
                    predicted_mid = y_norm_pred * global_std + global_mean
                    print("SQUID_INK - Predicted next mid price:", predicted_mid)
                    
                    # Generate trade signal.
                    threshold = 1.0  # Adjust threshold as needed.
                    if predicted_mid > current_mid + threshold:
                        if order_depth.sell_orders:
                            best_ask = min(order_depth.sell_orders.keys())
                            print("SQUID_INK - Buy signal. Placing BUY order for 1 unit at", best_ask)
                            orders.append(Order(product, best_ask, 1))
                        else:
                            print("SQUID_INK - Buy signal but no sell orders available.")
                    elif predicted_mid < current_mid - threshold:
                        if order_depth.buy_orders:
                            best_bid = max(order_depth.buy_orders.keys())
                            print("SQUID_INK - Sell signal. Placing SELL order for 1 unit at", best_bid)
                            orders.append(Order(product, best_bid, -1))
                        else:
                            print("SQUID_INK - Sell signal but no buy orders available.")
                    else:
                        print("SQUID_INK - Prediction within threshold. No trade executed.")
                result[product] = orders
        
        # Update and encode persistent state.
        traderData = jsonpickle.encode(trader_state)
        conversions = 0  # No conversion operations.
        return result, conversions, traderData
