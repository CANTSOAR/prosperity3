def sigmoid(x):
    return 1.0 / (1.0 + np.exp(-x))

def lstm_forward(x_seq):
    """
    Compute the forward pass of a single-layer LSTM for a sequence.
    x_seq: numpy array of shape (seq_length,) containing normalized input values.
    Returns a scalar: the predicted normalized output.
    """
    H = hidden_size
    # Initialize hidden and cell state as zero vectors.
    h = np.zeros((H,), dtype=np.float32)
    c = np.zeros((H,), dtype=np.float32)
    
    # Unroll over time steps (each x is a scalar)
    for x in x_seq:
        # Reshape x to (1,)
        x_t = np.array([x], dtype=np.float32)
        # Compute combined linear transformation:
        # Gates = W_ih * x_t + b_ih + W_hh * h + b_hh
        gates = np.dot(W_ih, x_t).reshape(-1) + b_ih + np.dot(W_hh, h) + b_hh  # shape (4*H,)
        # Split gates into four parts:
        i_gate = sigmoid(gates[0:H])
        f_gate = sigmoid(gates[H:2*H])
        g_gate = np.tanh(gates[2*H:3*H])
        o_gate = sigmoid(gates[3*H:4*H])
        # Update cell state and hidden state.
        c = f_gate * c + i_gate * g_gate
        h = o_gate * np.tanh(c)
    # Final output: use the last hidden state passed through a fully connected layer.
    y_norm = np.dot(W_fc, h) + b_fc  # shape (1,)
    return y_norm[0]  # Return scalar

